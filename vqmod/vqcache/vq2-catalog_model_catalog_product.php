// BOF - Zappo - Group Discounts - TWO LINES - Added group for Logged(-out) Customer to cache name
		$group_id = ($this->customer->isLogged() || !$this->config->get('config_discount_login')) ? '-' . $customer_group_id : '';
		$product_data = $this->cache->get('product.latest.' . (int)$this->config->get('config_language_id') . '.' . (int)$this->config->get('config_store_id') . '.' . $customer_group_id . $group_id . '.' . (int)$limit);

